#include "../../rtv1.h"

t_light			*create_light(t_v *c)
{
	t_light		*s;

	s = (t_light *)malloc(sizeof(t_light) + 1);
	if (s)
	{
		s->c = c;
		return (s);
	}
	exit(1);
}
