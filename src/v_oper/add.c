#include "../../rtv1.h"

void			add(t_v *v1, t_v *v2, t_v *res)
{
	if (res)
	{
		res->x = v1->x + v2->x;
		res->y = v1->y + v2->y;
		res->z = v1->z + v2->z;
	}
}
