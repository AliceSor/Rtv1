#include "../../rtv1.h"

double				scalar_mult(t_v *v1, t_v *v2)
{
	double			res;

	res = 0;
	if (v1 && v2)
		res = (v1->x * v2->x) + v1->y * v2->y + v1->z * v2->z;
	return (res);
}
